package Loops;

public class For_Loop2 
{
	//print odd numbers from 1 to 52
	public static void main(String[] args) 
	{          
		//loop body
		for(int p=1; p<=52; p=p+2)                            //for(starting condition; end condition ,incriment/decriment)
		{
			System.out.println(p);
			System.out.println("*********");             
		}
		
	}

}
