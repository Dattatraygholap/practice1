package Loops;

public class Do_while1 
{
	//print 1 to 5
	public static void main(String[] args) 
	{
		int i=1;       //starting condition
		
		do
		{
			System.out.println(i);
			i++;                        //incriment/decriment
		}
		while(i<=5);                    //end condition
		
	}

}
