package Loops;

public class While_Loop1  
{
	//Print 1 to 5 no.
	public static void main(String[] args)
	{
		int i=1;            //start condition
		
		while(i<=5)         //end condition
		{
			System.out.println(i);
			i++;                      //incriment/decriment
		}
		
	}

}
