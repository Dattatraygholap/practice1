package Loops;

public class While_Loop5
{
	         //Print add no 99 to 1
	public static void main(String[]args)
	{
		
		int i=99;          //starting condition
		
		while(i>=1)        //ending condition
		
		{
		   System.out.println(i);
			i=i-2;                   //incriment/decriment
		}
	}

}
